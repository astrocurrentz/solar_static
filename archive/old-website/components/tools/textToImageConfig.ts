import {
  TEXT_TO_IMG_POST_CONFIG,
  getTextToImageContentPageCount,
  getTextToImageParagraphContentWidth,
  getTextToImageParagraphFont,
} from '../../shared/text-to-img-post-config.mjs';

export const TEXT_TO_IMG_RENDER_ENDPOINT = import.meta.env.VITE_TEXT_TO_IMG_RENDER_ENDPOINT || TEXT_TO_IMG_POST_CONFIG.endpoint;
export const MAX_TOTAL_TEXT_LENGTH = TEXT_TO_IMG_POST_CONFIG.maxTotalTextLength;
export const MAX_TOTAL_PAGE_COUNT = TEXT_TO_IMG_POST_CONFIG.maxTotalPageCount;
export const MAX_CONTENT_PAGE_COUNT = getTextToImageContentPageCount();
export const MAX_UNITS_PER_LINE = TEXT_TO_IMG_POST_CONFIG.maxUnitsPerLine;
export const FIRST_PAGE_MAX_LINES = TEXT_TO_IMG_POST_CONFIG.firstPageMaxLines;
export const CONTINUATION_PAGE_MAX_LINES = TEXT_TO_IMG_POST_CONFIG.continuationPageMaxLines;
export const RENDER_OUTPUT_HEIGHT = TEXT_TO_IMG_POST_CONFIG.outputHeight;
export const RENDER_BODY_PADDING_TOP = TEXT_TO_IMG_POST_CONFIG.bodyPadding.top;
export const RENDER_BODY_PADDING_BOTTOM = TEXT_TO_IMG_POST_CONFIG.bodyPadding.bottom;
export const RENDER_HEADER_HEIGHT_FIRST = TEXT_TO_IMG_POST_CONFIG.header.minHeightTitle;
export const RENDER_HEADER_HEIGHT_CONTINUATION = TEXT_TO_IMG_POST_CONFIG.header.minHeightContinuation;
export const RENDER_HEADER_PADDING_TOP_FIRST = TEXT_TO_IMG_POST_CONFIG.header.paddingTopTitle;
export const RENDER_PARAGRAPH_MARGIN_TOP_FIRST = TEXT_TO_IMG_POST_CONFIG.paragraph.marginTopTitle;
export const RENDER_PARAGRAPH_MARGIN_TOP_CONTINUATION = TEXT_TO_IMG_POST_CONFIG.paragraph.marginTopContinuation;
export const RENDER_FOOTER_MARGIN_TOP = TEXT_TO_IMG_POST_CONFIG.footer.marginTop;
export const RENDER_FOOTER_PADDING_TOP = TEXT_TO_IMG_POST_CONFIG.footer.paddingTop;
export const RENDER_FOOTER_TEXT_HEIGHT = TEXT_TO_IMG_POST_CONFIG.footer.textHeight;
export const RENDER_PARAGRAPH_PADDING = TEXT_TO_IMG_POST_CONFIG.paragraph.padding;
export const RENDER_PARAGRAPH_FONT_SIZE = TEXT_TO_IMG_POST_CONFIG.paragraph.fontSize;
export const RENDER_PARAGRAPH_LINE_HEIGHT = TEXT_TO_IMG_POST_CONFIG.paragraph.lineHeight;
export const RENDER_PARAGRAPH_CONTENT_WIDTH = getTextToImageParagraphContentWidth();
export const RENDER_PARAGRAPH_LETTER_SPACING_PX = RENDER_PARAGRAPH_FONT_SIZE * TEXT_TO_IMG_POST_CONFIG.paragraph.letterSpacingRatio;
export const RENDER_PARAGRAPH_FONT = getTextToImageParagraphFont();
export const RENDER_FIRST_PAGE_SAFE_LINE_LIMIT = Math.max(
  1,
  Math.floor((
    RENDER_OUTPUT_HEIGHT
    - RENDER_BODY_PADDING_TOP
    - RENDER_BODY_PADDING_BOTTOM
    - RENDER_HEADER_HEIGHT_FIRST
    - RENDER_HEADER_PADDING_TOP_FIRST
    - RENDER_PARAGRAPH_MARGIN_TOP_FIRST
    - RENDER_FOOTER_MARGIN_TOP
    - RENDER_FOOTER_PADDING_TOP
    - RENDER_FOOTER_TEXT_HEIGHT
    - (RENDER_PARAGRAPH_PADDING * 2)
  ) / (RENDER_PARAGRAPH_FONT_SIZE * RENDER_PARAGRAPH_LINE_HEIGHT)) - 1,
);
export const RENDER_CONTINUATION_PAGE_SAFE_LINE_LIMIT = Math.max(
  1,
  Math.floor((
    RENDER_OUTPUT_HEIGHT
    - RENDER_BODY_PADDING_TOP
    - RENDER_BODY_PADDING_BOTTOM
    - RENDER_HEADER_HEIGHT_CONTINUATION
    - RENDER_PARAGRAPH_MARGIN_TOP_CONTINUATION
    - RENDER_FOOTER_MARGIN_TOP
    - RENDER_FOOTER_PADDING_TOP
    - RENDER_FOOTER_TEXT_HEIGHT
    - (RENDER_PARAGRAPH_PADDING * 2)
  ) / (RENDER_PARAGRAPH_FONT_SIZE * RENDER_PARAGRAPH_LINE_HEIGHT)) - 1,
);
